# Functions
def clear():
    """Clears the terminal"""
    os.system('cls' if os.name == 'nt' else 'clear')


def pick_person(list_of_dictionaries):
    """Picks a person out of a list of dictionaries and returns a tuple with:
    name, followers, description and country."""
    global picks
    while True:
        index_number = random.randint(0, len(list_of_dictionaries) - 1)
        if index_number not in picks:
            break
    name = list_of_dictionaries[index_number]["name"]
    followers = list_of_dictionaries[index_number]["follower_count"]
    description = list_of_dictionaries[index_number]["description"]
    country = list_of_dictionaries[index_number]["country"]
    return name, followers, description, country


