# Random word generation
chosen_word = random.choice(word_list)
length_chosen_word = len(chosen_word)

# Variables
hit = 0
mistakes = 0
guesses = []
blank = list("_" * length_chosen_word)

# Main Loop
print(logo)
print(f"The word is: {''.join(blank)}")
while True:
    guess = input("\nGuess a letter: ").lower()
    os.system('cls' if os.name == 'nt' else 'clear')
    # Right guess
    if guess not in guesses and guess in chosen_word:
        print(stages[6 - mistakes])
        print("\nRight guess!")
        guesses.append(guess)  # Anti-cheat

        # Go through the letters
        for n, letter in enumerate(chosen_word):
            if guess == letter:
                blank[n] = letter
                hit += 1

        # Check for win
        if hit == length_chosen_word:
            print(f"\nYou win!\nThe word was: {chosen_word}!")
            break

        # Display word so far
        print("".join(blank))
        continue

    # Wrong guess
    else:
        print("Wrong guess! You lost a life!")
        mistakes += 1

        # Checks for mistakes
        if mistakes == 6:
            print(stages[0])
            print(f"You lose.\nThe word was: {chosen_word}.")
            break

    print(stages[6 - mistakes])
    print("".join(blank))
